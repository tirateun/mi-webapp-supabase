// src/Users.tsx
import { useEffect, useState } from "react";
import { supabase } from "./supabaseClient";

export default function Users() {
  const [users, setUsers] = useState<any[]>([]);
  const [fullName, setFullName] = useState("");
  const [email, setEmail] = useState("");
  const [role, setRole] = useState("internal");
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");
  const [success, setSuccess] = useState("");
  const [currentRole, setCurrentRole] = useState(""); // üîπ Para saber si el usuario actual es admin

  // üìå Cargar usuarios existentes desde la tabla profiles
  const fetchUsers = async () => {
    const { data, error } = await supabase.from("profiles").select("*");
    if (!error) setUsers(data || []);
  };

  // üìå Obtener rol del usuario actual - Versi√≥n corregida
  const fetchCurrentUserRole = async () => {
    const {  userData } = await supabase.auth.getUser();
    if (userData?.user) {
      // ‚úÖ CORREGIDO: Desestructurar `data` y `error` del resultado de `.single()`
      const {  profile, error: profileError } = await supabase
        .from("profiles")
        .select("role")
        .eq("id", userData.user.id)
        .single();

      // Opcional: manejar el error si no se encuentra el perfil o hay otro problema
      if (profileError) {
        console.error("Error obteniendo el perfil del usuario:", profileError);
        setCurrentRole("");
        return; // Salir de la funci√≥n si hay error
      }

      // `profile` ahora contiene el objeto `{ role: "..." }` o es `null` si no se encontr√≥
      setCurrentRole(profile?.role || "");
    }
  };

  useEffect(() => {
    fetchUsers();
    fetchCurrentUserRole();
  }, []);

  // üìå Crear nuevo usuario con contrase√±a temporal - Versi√≥n corregida
  const handleAddUser = async () => {
    setLoading(true);
    setError("");
    setSuccess("");

    try {
      // ‚úÖ Obtener el token de sesi√≥n actual - Desestructuraci√≥n expl√≠cita
      const { data, error: sessionError } = await supabase.auth.getSession();

      // ‚úÖ Verificar que haya una sesi√≥n activa y un token v√°lido
      if (sessionError || !data.session || !data.session.access_token) {
        console.error("‚ùå No hay sesi√≥n activa o el token es inv√°lido:", data, sessionError);
        alert("‚ùå No hay sesi√≥n activa. Por favor, inicia sesi√≥n como administrador.");
        return;
      }

      const token = data.session.access_token;
      if (!token) throw new Error("No hay sesi√≥n activa.");

      const tempPassword = "Temporal123!";

      const response = await fetch(
        `${import.meta.env.VITE_SUPABASE_URL}/functions/v1/create-user`,
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${token}`,
          },
          body: JSON.stringify({
            email,
            password: tempPassword,
            full_name: fullName,
            role,
          }),
        }
      );

      const result = await response.json();
      if (!response.ok) throw new Error(result.error || "Error en create-user");

      const { error: updateError } = await supabase
        .from("profiles")
        .update({ must_change_password: true })
        .eq("email", email);

      if (updateError) throw updateError;

      setSuccess(
        `‚úÖ Usuario creado exitosamente. Contrase√±a temporal: ${tempPassword}`
      );
      setFullName("");
      setEmail("");
      setRole("internal");
      fetchUsers();
    } catch (err: any) {
      setError(err.message);
    }

    setLoading(false);
  };

  // üóëÔ∏è Eliminar usuario completamente (auth + profiles) - Versi√≥n corregida y segura
  const handleDeleteUser = async (userId: string) => {
    if (!confirm("¬øSeguro que deseas eliminar este usuario?")) return;

    try {
      // ‚úÖ Obtener el token de sesi√≥n actual - Desestructuraci√≥n expl√≠cita
      const { data, error: sessionError } = await supabase.auth.getSession();

      // ‚úÖ Verificar que haya una sesi√≥n activa y un token v√°lido
      if (sessionError || !data.session || !data.session.access_token) {
        console.error("‚ùå No hay sesi√≥n activa o el token es inv√°lido:", data, sessionError);
        alert("‚ùå No hay sesi√≥n activa. Por favor, inicia sesi√≥n como administrador.");
        return;
      }

      // Opcional: Puedes verificar si el token no est√° expirado mirando `data.session.expires_at`
      const now = new Date().getTime() / 1000; // Timestamp actual en segundos
      if (data.session.expires_at && data.session.expires_at < now) {
        console.error("‚ùå La sesi√≥n ha expirado.");
        alert("‚ùå La sesi√≥n ha expirado. Por favor, vuelve a iniciar sesi√≥n.");
        // Aqu√≠ puedes redirigir al login si lo deseas
        return;
      }

      console.log("Enviando token:", data.session.access_token); // Para depurar
      console.log("Enviando user_id:", userId); // Para depurar

      const res = await fetch(
        `${import.meta.env.VITE_SUPABASE_URL}/functions/v1/delete-user`,
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            // ‚úÖ CORREGIDO: Usar el token de sesi√≥n
            Authorization: `Bearer ${data.session.access_token}`,
          },
          body: JSON.stringify({ user_id: userId }),
        }
      );

      const dataRes = await res.json();
      if (!res.ok) {
        console.error("Respuesta de la funci√≥n:", dataRes); // Para depurar
        throw new Error(dataRes.error || "Error desconocido");
      }

      alert("‚úÖ Usuario eliminado correctamente");
      fetchUsers(); // Refresca la lista de usuarios
    } catch (err: any) {
      console.error("Error al eliminar usuario:", err); // Muestra el error en consola
      alert("‚ùå Error al eliminar usuario: " + err.message);
    }
  };

  return (
    <div id="usuarios">
      <h2>üë§ Lista de Usuarios</h2>

      {/* Formulario para agregar nuevo usuario */}
      <div
        style={{
          marginTop: "20px",
          padding: "20px",
          border: "1px solid #ccc",
          borderRadius: "10px",
          maxWidth: "500px",
        }}
      >
        <h3>‚ûï Agregar Usuario</h3>
        <input
          type="text"
          placeholder="Nombre completo"
          value={fullName}
          onChange={(e) => setFullName(e.target.value)}
          style={{ margin: "5px", padding: "8px", width: "100%" }}
        />
        <input
          type="email"
          placeholder="Correo"
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          style={{ margin: "5px", padding: "8px", width: "100%" }}
        />
        <select
          value={role}
          onChange={(e) => setRole(e.target.value)}
          style={{ margin: "5px", padding: "8px", width: "100%" }}
        >
          <option value="admin">Administrador</option>
          <option value="internal">Interno</option>
          <option value="external">Externo</option>
        </select>
        <button
          onClick={handleAddUser}
          disabled={loading}
          style={{
            marginTop: "10px",
            padding: "10px",
            background: "#3b82f6",
            color: "white",
            border: "none",
            borderRadius: "8px",
            cursor: "pointer",
            width: "100%",
          }}
        >
          {loading ? "Guardando..." : "Guardar"}
        </button>

        {error && <p style={{ color: "red" }}>‚ùå {error}</p>}
        {success && <p style={{ color: "green" }}>{success}</p>}
      </div>

      {/* Tabla de usuarios */}
      <h3 style={{ marginTop: "30px" }}>Usuarios Registrados</h3>
      <table
        style={{
          width: "100%",
          borderCollapse: "collapse",
          marginTop: "10px",
        }}
      >
        <thead>
          <tr style={{ background: "#f1f1f1" }}>
            <th style={{ border: "1px solid #ccc", padding: "8px" }}>Nombre</th>
            <th style={{ border: "1px solid #ccc", padding: "8px" }}>Correo</th>
            <th style={{ border: "1px solid #ccc", padding: "8px" }}>Rol</th>
            <th style={{ border: "1px solid #ccc", padding: "8px" }}>
              ¬øDebe cambiar contrase√±a?
            </th>
            <th style={{ border: "1px solid #ccc", padding: "8px" }}>
              Actualizado
            </th>
            {currentRole === "admin" && (
              <th style={{ border: "1px solid #ccc", padding: "8px" }}>
                Acciones
              </th>
            )}
          </tr>
        </thead>
        <tbody>
          {users.length === 0 ? (
            <tr>
              <td colSpan={6} style={{ textAlign: "center", padding: "10px" }}>
                No hay usuarios registrados.
              </td>
            </tr>
          ) : (
            users.map((u) => (
              <tr key={u.id}>
                <td style={{ border: "1px solid #ccc", padding: "8px" }}>
                  {u.full_name}
                </td>
                <td style={{ border: "1px solid #ccc", padding: "8px" }}>
                  {u.email}
                </td>
                <td style={{ border: "1px solid #ccc", padding: "8px" }}>
                  {u.role}
                </td>
                <td style={{ border: "1px solid #ccc", padding: "8px" }}>
                  {u.must_change_password ? "‚úÖ S√≠" : "‚ùå No"}
                </td>
                <td style={{ border: "1px solid #ccc", padding: "8px" }}>
                  {u.updated_at
                    ? new Date(u.updated_at).toLocaleString()
                    : "-"}
                </td>
                {currentRole === "admin" && (
                  <td
                    style={{
                      border: "1px solid #ccc",
                      padding: "8px",
                      textAlign: "center",
                    }}
                  >
                    <button
                      // ‚úÖ CORREGIDO: Usa u.id
                      onClick={() => handleDeleteUser(u.id)}
                      style={{
                        background: "#ef4444",
                        color: "white",
                        border: "none",
                        borderRadius: "6px",
                        padding: "6px 10px",
                        cursor: "pointer",
                      }}
                    >
                      üóëÔ∏è Eliminar
                    </button>
                  </td>
                )}
              </tr>
            ))
          )}
        </tbody>
      </table>
    </div>
  );
}


